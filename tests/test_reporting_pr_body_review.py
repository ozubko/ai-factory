def test_report_leads_with_risk_assessment_and_factory_vs_agent_claims(
    git_repo: Path, tmp_path: Path
) -> None:
    state_dir = tmp_path / "state"

    exit_code = _run(git_repo, state_dir, "fix a typo in the README")

    assert exit_code == 0
    report_text = (_load_run_dir(state_dir) / "report.md").read_text()

    risk_pos = report_text.index("## Risk Assessment")
    claims_pos = report_text.index("## Factory-verified vs agent-claimed")
    verify_pos = report_text.index("## Verification Gate")
    assert risk_pos < claims_pos < verify_pos
    assert "Factory-verified (authoritative)" in report_text
    assert "Agent-claimed (advisory only)" in report_text


