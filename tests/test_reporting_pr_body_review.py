def test_review_contract_violation_halts_with_evidence_and_never_touches_target(
    git_repo: Path, tmp_path: Path
) -> None:
    state_dir = tmp_path / "state"

    exit_code = _run(
        git_repo,
        state_dir,
        "fix a typo in the README",
        backend="fake-review-violator",
        extra_args=["--review"],
    )

    assert exit_code == 1
    run_dir = _load_run_dir(state_dir)
    metadata = _load_run_metadata(state_dir)

    assert metadata["phases"]["plan"]["status"] == "succeeded"
    assert metadata["phases"]["implement"]["status"] == "succeeded"
    assert metadata["outcome"] == "contract_violation"
    assert metadata["phases"]["review"]["status"] == "contract_violation"

    violation_patch = (run_dir / "contract-violation.patch").read_text()
    assert "FAKE_AGENT_REVIEW_VIOLATION.md" in violation_patch

    report_text = (run_dir / "report.md").read_text()
    assert "Contract Violation" in report_text

    status = subprocess.run(
        ["git", "status", "--porcelain"], cwd=git_repo, capture_output=True, text=True
    ).stdout
    assert status == ""


# --- Never merges/pushes/opens a PR/remote op ---------------------------------------


def test_no_push_merge_or_remote_operation_occurs(git_repo: Path, tmp_path: Path) -> None:
    state_dir = tmp_path / "state"

    _run(git_repo, state_dir, "fix a typo in the README", extra_args=["--review"])
    _run(git_repo, state_dir, "add an OAuth login flow", extra_args=["--review"])

    # The target repo has no remotes and is left exactly where it started;
    # nothing in the run path can have pushed, merged, or opened a PR.
    remotes = subprocess.run(
        ["git", "remote"], cwd=git_repo, capture_output=True, text=True
    ).stdout
    assert remotes == ""
    branch_before = subprocess.run(
        ["git", "rev-parse", "--abbrev-ref", "HEAD"], cwd=git_repo, capture_output=True, text=True
    ).stdout.strip()
    assert not branch_before.startswith("factory/")
