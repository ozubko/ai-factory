def test_pr_body_is_written_and_forge_neutral_with_expected_sections(
    git_repo: Path, tmp_path: Path
) -> None:
    state_dir = tmp_path / "state"

    exit_code = _run(git_repo, state_dir, "fix a typo in the README")

    assert exit_code == 0
    pr_body_path = _load_run_dir(state_dir) / "pr-body.md"
    assert pr_body_path.is_file()
    pr_body_text = pr_body_path.read_text()

    for heading in (
        "## Summary",
        "## What Changed",
        "## Why",
        "## Verification",
        "## Test Coverage Notes",
        "## Risks & Follow-ups",
        "## Human Review Notes",
    ):
        assert heading in pr_body_text

    # Forge-neutral: no GitHub/GitLab-specific syntax.
    assert "#" not in pr_body_text.replace("##", "").replace("#!", "")
    assert "@" not in pr_body_text

    assert "Generated by ai-factory" in pr_body_text
    assert "verify before merging" in pr_body_text


